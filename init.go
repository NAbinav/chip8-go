func (c *Chip8) Table0() {
	switch c.Opcode {
	case 0x00E0:
		c.OP_00E0()
	case 0x00EE:
		c.OP_00EE()
	default:
		// ignored
	}
}
func (c *Chip8) Table8() {
	c.table8[c.Opcode&0x000F]()
}
func (c *Chip8) TableE() {
	c.tableE[c.Opcode&0x000F]()
}

func (c *Chip8) TableF() {
	c.tableF[c.Opcode&0x00FF]()
}
func (c *Chip8) OP_NULL() {
	panic("Unknown opcode")
}
